package learningJava.basics;

/**
 * Octal & Hexadecimal data types
 * @author Namra Ansari
 */
public class DataTypes {
    public static void main(String args[]){
        
        // using Octal & Hexadecimal Number System
        int oct = 010;  // number starting with "0" is octal number
        System.out.printf("The octal number 010 in decimal= %d",oct).println();
        
        int hex = 0X10FA;  // number starting with "0X" is octal number
        System.out.printf("The hexadecimal number 0X10FA in decimal= %d",hex).println();
        
    }
}
